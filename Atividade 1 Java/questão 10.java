package com.mycompany.projeto2;

import java.util.Scanner;

/**
 *
 * @author Aluno01
 */
public class atividade11 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        // Passo 1: Ler o valor do produto e a porcentagem de desconto
        System.out.print("Digite o valor do produto: ");
        double valorProduto = scanner.nextDouble();

        System.out.print("Digite a porcentagem de desconto (ex: 10 para 10%): ");
        double porcentagemDesconto = scanner.nextDouble();

        // Passo 2: Calcular o valor do desconto
        double valorDesconto = (valorProduto * porcentagemDesconto) / 100;

        // Passo 3: Calcular o valor final com desconto
        double valorFinal = valorProduto - valorDesconto;

        // Passo 4: Exibir o valor final com desconto
        System.out.println("Valor final com desconto: " + valorFinal);

        scanner.close();
    }
}
